uint8_t g_Rx_data[SIZE], g_Tx_Data[SIZE];
uint16_t g_parsed_data[6];
uint64_t g_temp_data;

union converted16{
        struct{
                uint8_t lsb; // first 8 bit of the 16 bit data
                uint8_t msb; // second 8 bit of the 16 bit data
        }raw;
        uint16_t data;

}mydata;

void parse_data(uint64_t temp_data)
{
	// converting decimal to binary
	for (int i = 0; i < 6; ++i) {
		uint16_t temp = 0;
		for (int k = 0; k < 10; ++k) {
			int remainder = temp_data % 2;
			temp_data /= 2;
			if(remainder)
				temp |= (1 << k);
			else
				temp |= (0 << k);
		}
		// parsing the data 6 x 10 bit
		g_parsed_data[i] = temp;
	}
}

void SPI_Data_Tx(uint16_t *data, size_t length)
{
	// converting the 16 bit data to 8 bit data
	for(int i = 0; i < length; ++i) {
			mydata.data = g_parsed_data[i];
			g_Tx_Data[2 * i + 1]	= mydata.raw.lsb;
			g_Tx_Data[2 * i + 2]	= mydata.raw.msb;
	}
	// pre-defined OPCODE and HEADER adding
	g_Tx_Data[2 * length + 2] = 2 * length;
	g_Tx_Data[2 * length + 3] = OPCODE;
	g_Tx_Data[2 * length + 4] = HEADER;

}


void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{


	int k = 0;

	// converting the obtained data to decimal
	for(int i = SIZE; i > 0 ; --i) {
		g_temp_data +=(uint64_t)((g_Rx_data[i-1]) * pow((double)10,(double)k));
		++k;
	}

	parse_data(g_temp_data);

	SPI_Data_Tx(g_parsed_data, sizeof(g_parsed_data)/sizeof(g_parsed_data[0]));

	HAL_SPI_Transmit(&hspi1, g_Tx_Data, 15, 100); // transmitting the parsed data
	hspi1.Instance->CR1 |= (1 << SPI_CR1_CRCNEXT); // to send CRC value as a last byte setting the CRCNEXT


}

